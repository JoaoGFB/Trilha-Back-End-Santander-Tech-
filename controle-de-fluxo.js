const numero1 = 100
const numero2 = 20

if (numero1 > 1) {
    console.log(`Este número é maior que 1`)
} 

if (numero1 >= 200) {
    console.log(`Este número é maior ou igual que 200`)
} else {
    console.log(`Este número é menor que 200`)
}

if (numero1 > numero2) {
    console.log(`O primeiro número é maior que o segundo`)
} else {
    console.log(`O segundo número é maior que o primeiro`)
}

if (numero1 == numero2) {
    console.log(`Os números são iguais em valor apenas`)
}
if (numero1 === numero2) {
    console.log(`Os números são iguais em valor e tipo de dado`)
}
